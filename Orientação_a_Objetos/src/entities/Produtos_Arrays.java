package entities;

public class Produtos_Arrays 
{

	private String nome;
	private double preco;
	


	public Produtos_Arrays(String nome, double preco) {
		this.nome = nome;
		this.preco = preco;
	}


	//gets setters
	public String getNome() {
		return nome;
	}


	public void setNome(String nome) {
		this.nome = nome;
	}




	public double getPreco() {
		return preco;
	}


	
	
	
	
	
	
}
